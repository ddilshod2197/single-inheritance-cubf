class Avto:
    def __init__(self, rahbar, korobka):
        self.rahbar = rahbar
        self.korobka = korobka

class AvtoMashina(Avto):
    def __init__(self, rahbar, korobka, model, rang):
        super().__init__(rahbar, korobka)
        self.model = model
        self.rang = rang

avto = AvtoMashina("Rahbar", "Korobka", "Model", "Rang")
print(avto.rahbar)
print(avto.korobka)
print(avto.model)
print(avto.rang)
